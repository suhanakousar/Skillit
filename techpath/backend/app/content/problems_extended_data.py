    """Compact problem factory — keeps the file readable."""
    return {
        "title": title,
        "slug": slug,
        "description": desc,
        "difficulty": diff,
        "tags": tags,
        "xp_reward": xp,
        "examples_json": examples,
        "test_cases_json": tests,
        "starter_code_json": {"python": starter},
        "hints_json": hints,
    }


